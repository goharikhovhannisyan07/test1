package classwork.student;

public class Student {

    String name;
    String surname;
    String phoneNumber;
    int age;
    String email;
    String courseName;

    public Student(String name, String surname, String phoneNumber, int age, String email, String courseName) {
        this.name = name;
        this.surname = surname;
        this.phoneNumber = phoneNumber;
        this.age = age;
        this.email = email;
        this.courseName = courseName;
    }
}
